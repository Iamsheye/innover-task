import type { NextPage } from "next";
import Head from "next/head";
import axios from "axios";
import { useForm } from "react-hook-form";
import { yupResolver } from "@hookform/resolvers/yup";
import { loginSchema } from "../utils/schemas";
import Button from "../components/Button/Button";
import Container from "../components/Container/Container";
import { FormGroup, FormInput, FormLabel } from "../components/Form";
import Title from "../components/Title/Title";
import ErrorText from "../components/ErrorText/ErrorText";

interface ILoginForm {
  email: string;
  pword: string;
}

const Login: NextPage = () => {
  const FormFields = [
    {
      label: "Email address",
      id: "email",
      type: "email",
      name: "email",
      placeholder: "Enter your email address",
    },
    {
      label: "Password",
      id: "pword",
      type: "password",
      name: "pword",
      placeholder: "Enter your password",
    },
  ];

  const {
    register,
    handleSubmit,
    formState: { errors },
  } = useForm<ILoginForm>({
    resolver: yupResolver(loginSchema),
  });

  const onSubmit = handleSubmit(async (data) => {
    try {
      const res = await axios.post("/user/login", {
        email: data.email,
        password: data.pword,
      });
      console.log(res.data);
      document.cookie = `userId=${res.data._id}`;
      // WRITE SUCCESSFUL TOAST HERE
    } catch (err) {
      // WRITE ERROR TOAST HERE
      console.log(err);
    }
  });

  return (
    <>
      <Head>
        <title>Techinnover Frontend Task | Login</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Container>
        <Title>Login</Title>
        <form onSubmit={onSubmit} autoComplete="off">
          {FormFields.map((field) => (
            <FormGroup key={field.id}>
              <FormLabel htmlFor={field.id}>{field.label}</FormLabel>
              <FormInput
                dark
                {...register(field.name as any)}
                // @ts-ignore
                error={errors[field.name]?.message}
                type={field.type}
                id={field.id}
                name={field.name}
                placeholder={field.placeholder}
              />
              {/* @ts-ignore */}
              <ErrorText>{errors[field.name]?.message}</ErrorText>
            </FormGroup>
          ))}
          <Button type="submit">Log in</Button>
        </form>
      </Container>
    </>
  );
};

export default Login;
